function infoJoin(firstName, lastName, age, residence, interest) {
    return "My name is " + firstName + " " + lastName + ". I am " + age + " years old and I live in " + residence + ". One of my hobbies is " + interest + ".";
}
console.log(infoJoin("Aleko", "Tirkia", 16, "Georgia", "programming"));
