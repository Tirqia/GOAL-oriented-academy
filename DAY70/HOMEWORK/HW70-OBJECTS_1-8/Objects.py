# 4. შექმენით ობიექტი, რომელიც წარმოადგენს მანქანას, თვისებებით: მწარმოებელი, მოდელი და მფლობელი ობიექტი, რომელსაც აქვს თვისებები: სახელი და ასაკი
car = {
    "make": "ბეემვე",
    "model": "გატრინოლებული",
    "owner": {
        "name": "ჯუმბერიკო",
        "age": 9
    }
}

# 5. მიიღეთ და გამოიტანეთ მანქანის მფლობელის სახელი და ასაკი
