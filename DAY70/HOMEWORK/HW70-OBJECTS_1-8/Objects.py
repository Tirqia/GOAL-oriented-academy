# 7. დაემატოს მეთოდი მანქანის ობიექტს, რომელიც გამოთვლის მანქანის ასაკს მიმდინარე წელს და მანქანის წარმოების წლიდან
def calculateCarAge(currentYear, manufactureYear):
    return currentYear - manufactureYear

# გამოიყენეთ ფუნქცია მანქანის ობიექტით
car_age = calculateCarAge(2024, 2018)  # ვივარაუდოთ, რომ მანქანა 2018 წელს იყო გამოშვებული
print("მანქანის ასაკი:", car_age)

